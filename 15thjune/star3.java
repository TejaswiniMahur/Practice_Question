
public class star3 {
    public static void main(String[] args) {
        int i=5;
        for (i=1;i<=5;i++){
            for(int j=1;j<=i;j++){
            System.out.print("*");
            }
            System.out.println(); 
        }
       for (i=2;i<=5;i++){
            for(int j=5;j>=i;j--){
            System.out.print("*");
            }
      
            System.out.println(); 
    }   }
 
}
